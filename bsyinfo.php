 <p style="font-size:16px"><b>COVERAGE:</b><br>

 Balika Samridhi Yojna is being implemented in both rural and urban areas.<br>

  <b>TARGET GROUP:</b><br>

Under Balika Samridhi Yojna girl children belonging to families below the poverty line are given benefit, who are born on or after 15th August,1997. The benefits are restricted to two girl children in a household irrespective of number of children in the household.<br>

  <b>PROCEDURE FOR OBTAINING THE BENEFIT:</b><br>

This scheme is being implemented through ICDS infrastructure in rural areas and through functionaries of Health Department in urban areas. The application forms are available with Anganwadi Workers in the villages and with Health functionaries in urban areas. The beneficiaries are required to submit the  filled in applications to these functionaries.<br>

  <b>DISBURSEMENT OF ASSISTANCE:</b><br>

The amounts of post birth grant and scholarships are to be deposited in an interest bearing account to be opened in the name of the beneficiary girl child and an officer designated in this behalf by the State Govt. in the nearest bank or post office.The amount should earn the maximum possible rate of interest.In this context the Public Provident Fund scheme or the National Saving Certificate is given higher priority.<br>

  <b>FACILITY OF WITHDRAWAL:</b><br>

A portion of the Post Birth Grant of Rs.500/- or the amount of the annual scholarship can be applied toward the sole purpose of paying the premium on an insurance policy in the name of the girl child under the Bhagyashri Balika Kalyan Bima Yojna. The amounts of annual scholarships can also be permitted to be utilised toward purchase of textbook or uniform for the girl child.The amount left after paymant shall be deposited in the account.<br>

  <b>PAYMENT AT MATURITY:</b><br>

On the girl child attaining 18 years of age and on production of a certificate from the Gram Panchayat /Municipality that she is unmarried on her eighteenth birthday, the implementing agency would authorise the bank or the post office authorities concerned to allow her to withdraw the amount standing in her name in the interest bearing account.<br>

  <b>WITHDRAWAL OF THE BENEFIT:</b><br>

In the event of the girl getting married before attaining the age of 18 years, she shall forgo the benefit of the amount of annual scholarships and the interest accrued thereon and shall stand entitled only to the Post Birth Grant amount of Rs.500/- and the interest accrued thereon. In the eventuality of the death of the girl child before attaining the age of 18 years, the accumulated amount in her account would be withdrawn.<br>

  <b>FUNDS :</b><br>

Prior to 1999-2000 , funds were being released to the Additional Deputy Commissioners by the Govt of India.However. thereafter the funds were being released to the State Govt.</p>
