                        <h3 class="page-title">
                  Balika Samridhi Yojana (BSY) 
               </h3>
               <p style="font-size:16px">The scheme of Balika Samridhi Yojana was launched by Govt. of India w.e.f. 15-8-97 with the following objectives:-<br>

                1. To change negative family and community attitudes towards the girl child at birth and
                towards her mother.<br>
                2. To improve enrolment and retention of girl children in schools.<br> 
                3. To raise the age at marriage of girls.<br>
                4. To assist the girl to undertake income generating activities.<br>

     

  COMPONENTS OF BALIKA SAMRIDHI YOJNA<br>

From the start of the scheme, the mothers of newborn girl children were given a post delivery grant of Rs. 500/- each in cash as a gift from the Government. There was also provision for Annual Scholarships to the girl child for attending school. But the details /guidelines for scholarships were not received.<br>

During 1999-2000, Govt. of India recast the scheme and the benefits and means of delivery have been redesigned to ensure that financial benefits accrue to the girl child. Now the girl children eligible under BSY are entitled to the following benefits:-<br>

1.   A post birth grant amount of Rs. 500/-.<br>

2.   When the girl child born on or after 15-08-1997 and covered under BSY starts attending
      the school, she will become entitled to annual scholorship as under for each successfully
       completed year of schooling:-<br>

