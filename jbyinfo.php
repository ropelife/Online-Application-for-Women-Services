                  JEEVANJYOTI BIMA YOJNA (JBY) 
               </h3>
               <p style="font-size:16px">The central government of India has launched a new life insurance scheme, Pradhan Mantri Jeevan Jyoti Bima Yojana, for the growth of the poor and low income section of society. As a pure term insurance plan, Pradhan Mantri Jeevan Jyoti Bima Yojana, is available for people between the age group of 18- 50 years.<br>

                PMJJBY is a renewal term insurance policy that, provides a yearly life insurance coverage of Rs. 2,00,000 in case of the demise of the insured person, at the most affordable premium rate of Rs. 330 per annum. In order to help you know more about the policy, we have discussed some of the key features and benefits offered by the policy.<br><br>

                <b>Salient Features of Pradhan Mantri Jeevan Jyoti Bima Yojana</b><br>
                1) The policy provides life coverage for 1 year.<br>
                2) The insured can renew the policy every year.<br>
                3) According to oneâ€™s own choice, the insured can walk out of the scheme at any time and rejoin it in future.<br>
                4) The policy offers a maximum sum assured of Rs2 lakh.<br>
                5) As compared to the other term insurance policy the plan offers very low premium rates per year i.e. Rs. 330. Moreover, the premium rate is equal for all age groups ranging from 18 to 50 years.<br><br>

               <b>Benefits Offered by Pradhan Mantri Jeevan Jyoti Bima Yojana</b><br>
               1) Death Benefit- In case of demise of the insured person the PMJJBY provides a death coverage of Rs2,00,000 to the beneficiary of the policy.<br>
               2) Maturity Benefit- As this is pure term insurance plan, PMJJBY does not offer any maturity or surrender benefit.<br>
               3) Tax Benefit- The premium paid towards the policy is eligible for tax deduction under section 80C of Income Tax Act. In case the insurance holder fails to submit form 15 G/15 H then any life insurance proceeds exceeding Rs. 1,00,000 will be taxable by 2%.<br>
               4) Risk Coverage- PMJJBY provides a risk coverage of 1 year. Nevertheless, as this is renewable policy it can be renewed yearly. Moreover, the policyholder can also opt for a longer duration more than a year by auto debit option linked to your saving bank account.<br><br>
                <b>The Eligibility Criteria of the policy are:-</b><br>
            1) Any person aged between 18- 50 years having a saving bank account can join this scheme through participating banks.<br>
2) Even if you have multiple bank accounts, you can subscribe this scheme by only one saving bank account.<br>
3) In order to avail the benefits offered by the policy, it is mandatory to link your adhaar card to the participatory bank account.<br>
4) Insurance buyers joining the scheme after the primary enrolment period ranging from 31st August 2015- 30th November 2015 will have to submit a self-attested medical certificate as a proof that he/she is not suffering from any critical illness mentioned in the policy declaration form.
