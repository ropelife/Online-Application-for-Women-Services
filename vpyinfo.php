<b style="font-size:16px">Benefits/Advantages of LIC Varishtha Pension Bima Yojana</b><br><br>
<p style="font-size:16px">The LIC Varishtha Pension Bima Yojana provides you with the following key benefits:<br>

1) Policies can be purchased by payment of a lump sum known as Purchase Price.<br>
2) Pensioner can choose between the pension amount and the purchase price.<br>
3) Guaranteed benefits: Pension will be paid depending on the mode of pension payment selected. The first pension will be paid after 1 month,  months, 6 months or 1 year from the date of purchase.<br>
4) Family Benefits: Pension can also be received by family including spouse and dependants.<br>
5) Loan: After completing 3 years of the policy, loan facility is available for a maximum of 75% of the purchase price. Interest on the loan will be recovered from the pension.<br>
6) Death benefits: Upon the death of the pensioner, the purchase price will be refunded.<br>



