               </h3>
                <p style="font-size: 16px">Government of India in the Union Budget 2014-2015, announced the revival of Varishtha Pension Bima Yojana. Excerpts from budget speech by Honrable Finance Minister are, "NDA Government during its last term in office had introduced the Varishtha Pension Bima Yojana (VPBY) as a pension scheme for senior citizens. Under the scheme a total number of 3.16 lakh annuitants are being benefited and corpus amounts to Rs.6,095 Crore. I propose to revive the scheme for a limited period from 15th August 2014 to 14th August 2015 for the benefit of citizens aged 60 years and above"<br>
                LIC of India has been given the sole privilege to operate this scheme.<br>

                The NDA Government during its last term in office had introduced the Varishtha Pension BimaYojana (VPBY) as a pension scheme for senior citizens. Under the scheme a total no. of 3.16 lakh annuitants are being benefited and the corpus amounts to Rs. 6,095 crore. For the benefit of citizens aged 60 years and above, the Hon'ble Finance Minister in his Budget Speech for the year 2014-15 proposed to revive the scheme for a limited period from 15 August, 2014 to 14 August, 2015. Accordingly, the revived Varishtha Pension BimaYojana (VPBY) was formally launched by the Finance Minister on 14.08.2014 and has been opened during the window stretching from 15th August, 2014 to 14th August, 2015. Thus all those who subscribe to the VPBY during this period will receive an assured guaranteed return of 9% under the policy. The scheme is administered through Life Insurance Corporation of India (LIC). Under the Scheme the subscribers on payment of a lump sum amount get pension at a guaranteed rate of 9% per annum (payable monthly). Any gap in the guaranteed return over the return generated by the LIC on the fund is compensated by Government of India by way of subsidy payment in the scheme. The scheme allows withdrawals of deposit amount by the annuitant after fifteen years of purchase of the policy.<br><br>

