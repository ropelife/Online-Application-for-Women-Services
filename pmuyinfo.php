                 PRADHAN MANTRI UJJWALA YOJANA (PMUY)
               </h3>
                <p style="font-size: 16px">Pradhan Mantri Ujjwala Yojana is an ambitious social welfare scheme of Narendra Modi Government launched on 1st May 2016 from Ballia in Uttar Pradesh. Under the PM Ujjwala Yojana, the government aims to provide LPG connections to BPL households in the country. The scheme is aimed at replacing the unclean cooking fuels mostly used in the rural India with the clean and more efficient LPG (Liquefied Petroleum Gas).<br>

                    Objectives of Pradhan Mantri Ujjwala Yojana<br>
                    Ujjwala Yojana is aimed at providing 5 Crore LPG connections in the name of women in BPL (Below Poverty Line) households across the country. The government has set a target of 5 Crore LPG connections to be distributed to the BPL households across the country under the scheme. Some of the objectives of the scheme are:<br><br>

                    1) Empowering women and protecting their health.<br>
                    2) Reducing the serious health hazards associated with cooking based on fossil fuel.<br>
                    3) Reducing the number of deaths in India due to unclean cooking fuel.<br>
                    4) Preventing young children from significant number of acute respiratory illnesses caused due to indoor air pollution by burning the fossil fuel.<br><br>

                    <b>Eligibility for PM Ujjwala Yojana</b><br>
                    The identification of eligible BPL families will be made on the basis of SECC-2011 data. However below is the basic eligibility criteria for the scheme.<br>

                    1) The applicant’s name should be in the list of SECC-2011 data.<br>
                    2) The applicant should be a women above the age of 18 years.<br>
                    3) The women applicant should belong to BPL (Below Poverty Line).<br>
                    4) The women applicant should have a saving bank account in any nationalized bank across the country.<br>
                    5) The applicant’s household should not already own a LPG connection in anyone’s name.<br><br>

                    <b>List of Documents Required for Pradhan Mantri Ujjwala Yojana</b><br>
                    1)   BPL Certificate Authorised by Panchayat Pradhan / Municipality Chairman*<br>
                    2)    BPL Ration Card*<br>
                    3)    One Photo ID (Aadhar Card or Voter ID Card)*<br>
                    4)    One Recent Passport Size Photograph*<br>
                    5)    Driving License<br>
                    6)    Lease Agreement<br>
                    7)    Telephone / Electricity / Water Bill<br>
                    8)    Ration Card<br>

* are the mandatory documents to prove the residence of the women applicant and her BPL status.<br><br>

                    The scheme will be implemented by the Ministry of Petroleum & Natural Gas. This is first time in the history that Ministry of Petroleum & Natural Gas is implementing such an enormous welfare scheme which will benefit Crores of women belonging to the poorest households.<br>

                    This Scheme would be implemented over three years, namely, the FY 2016-17, 2017-18 and 2018-19.





