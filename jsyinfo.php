                        <h3 class="page-title">
                  Janani Suraksha Yojana (JSY) 
               </h3>

               <p style="font-size:16px">JananiSurakshaYojana (JSY) is a safe motherhood intervention under the National Health Mission. It is being implemented with the objective of reducing maternal and neonatal mortality by promoting institutional delivery among poor pregnant women. The scheme, launched on 12 April 2005 by the Hon’ble Prime Minister, is under implementation in all states and Union Territories (UTs), with a special focus on Low Performing States (LPS).<br>

                The scheme focuses on the poor pregnant woman with special dispensation for states having low institutional delivery rates namely the states of Uttar Pradesh, Uttaranchal, Bihar, Jharkhand, Madhya Pradesh, Chhattisgarh, Assam, Rajasthan, Orissa and Jammu and Kashmir. While these states have been named as Low Performing States (LPS), the remaining states have been named as High performing States (HPS).<br>
                Tracking Each Pregnancy: Each beneficiary registered under this Yojana should have a JSY card along with a MCH card. ASHA/AWW/ any other identified link worker under the overall supervision of the ANM and the MO, PHC should mandatorily prepare a micro-birth plan.  This will effectively help in monitoring Antenatal Check-up, and the post delivery care.<br>
                 Eligibility for Cash Assistance: BPL Certification – This is required in all HPS states. However, where BPL cards have not yet been issued or have not been updated, States/UTs would formulate a simple criterion for certification of poor and needy status of the expectant mother’s family by empowering the gram pradhan or ward member.<br>

                JSY is a centrally sponsored scheme, which integrates cash assistance with delivery and post-delivery care. The Yojana has identified Accredited Social Health Activist (ASHA) as an effective link between the government and pregnant women.<br>

                Important Features of JSY<br>
                The scheme focuses on poor pregnant woman with a special dispensation for states that have low institutional delivery rates, namely, the    states of Uttar Pradesh, Uttarakhand, Bihar, Jharkhand, Madhya Pradesh, Chhattisgarh, Assam, Rajasthan, Orissa, and Jammu and Kashmir. While   these states have been named Low Performing States (LPS), the remaining states have been named High Performing states (HPS).<br><br>

